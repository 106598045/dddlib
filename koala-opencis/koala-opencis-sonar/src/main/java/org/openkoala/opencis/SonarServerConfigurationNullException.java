package org.openkoala.opencis;

public class SonarServerConfigurationNullException extends RuntimeException {

	private static final long serialVersionUID = 1790068873734550604L;

	public SonarServerConfigurationNullException() {
		super();
	}

	public SonarServerConfigurationNullException(String message, Throwable cause) {
		super(message, cause);
	}

	public SonarServerConfigurationNullException(String message) {
		super(message);
	}

	public SonarServerConfigurationNullException(Throwable cause) {
		super(cause);
	}
	
}
