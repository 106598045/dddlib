package org.openkoala.opencis;

public class JenkinsServerConfigurationNullException extends RuntimeException {

	private static final long serialVersionUID = -4582764013154617433L;

	public JenkinsServerConfigurationNullException() {
		super();
	}

	public JenkinsServerConfigurationNullException(String message, Throwable cause) {
		super(message, cause);
	}

	public JenkinsServerConfigurationNullException(String message) {
		super(message);
	}

	public JenkinsServerConfigurationNullException(Throwable cause) {
		super(cause);
	}

}
