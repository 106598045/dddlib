package org.openkoala.opencis;

public class PropertyIllegalException extends RuntimeException {

	private static final long serialVersionUID = -4254185676344745473L;

	public PropertyIllegalException() {
		super();
	}

	public PropertyIllegalException(String message, Throwable cause) {
		super(message, cause);
	}

	public PropertyIllegalException(String message) {
		super(message);
	}

	public PropertyIllegalException(Throwable cause) {
		super(cause);
	}
	
}
