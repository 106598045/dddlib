package org.openkoala.opencis;

public class JenkinsAdminLoginException extends RuntimeException {

	private static final long serialVersionUID = 7273327892714359892L;

	public JenkinsAdminLoginException() {
		super();
	}

	public JenkinsAdminLoginException(String message, Throwable cause) {
		super(message, cause);
	}

	public JenkinsAdminLoginException(String message) {
		super(message);
	}

	public JenkinsAdminLoginException(Throwable cause) {
		super(cause);
	}

}
