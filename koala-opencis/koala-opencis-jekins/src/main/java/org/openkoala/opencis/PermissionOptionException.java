package org.openkoala.opencis;

public class PermissionOptionException extends RuntimeException {

	private static final long serialVersionUID = -6361189421736526712L;

	public PermissionOptionException() {
		super();
	}

	public PermissionOptionException(String message, Throwable cause) {
		super(message, cause);
	}

	public PermissionOptionException(String message) {
		super(message);
	}

	public PermissionOptionException(Throwable cause) {
		super(cause);
	}

}
